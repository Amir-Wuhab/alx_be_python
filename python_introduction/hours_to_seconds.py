# Create a file named hours_to_seconds.py

# Define a variable named hours and assign it a value.
hours = 2

# Calculate the number of seconds in the given hours.
# (60 minutes per hour * 60 seconds per minute = 3600 seconds per hour)
seconds = hours * 3600

# Print the result in the specified format.
print(f"{hours} hour(s) is {seconds} seconds.")